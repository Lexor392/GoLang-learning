package main

import "fmt"

/* Булевое значение */
func main() {
	var variable bool    // По умолчанию, булевое значение будет равно false
	var test bool = true // Присвоено значение true булевой переменной
	search := true       // Присвоено значение true быстрой переменной
	var find = true      // Стандартная переменная которой присвоенно булевое значение true

	fmt.Println(variable)
	fmt.Println(test)
	fmt.Println(search)
	fmt.Println(find)
}

package main

import "fmt"

/* Примеры различных типов данных */
func main() {
	var a bool = true       // Булевое значение (true & false)
	var b int = 12          // Целочисленное значение
	var c float32 = 5.25    // Значение с плавающей запятой
	var d string = "Hello!" // Строчное значение

	fmt.Println("Bool:", a)
	fmt.Println("Integer (int):", b)
	fmt.Println("Float:", c)
	fmt.Println("String:", d)
}
